function extractPatterns(html: string): string[] {
  const patterns: string[] = [];
  
  // Check for common article containers
  const containerPatterns = [
    /class="[^"]*(?:article|post|nota|noticia|news|entry)[^"]*"/gi,
    /<article[^>]*>/gi,
    /class="[^"]*(?:card|item)[^"]*"[^>]*>/gi
  ];
  
  for (const pattern of containerPatterns) {
    const matches = html.match(pattern);
    if (matches) {
      patterns.push(...matches.slice(0, 3));
    }
  }
  
  return [...new Set(patterns)].slice(0, 10);
}

function generateSuggestions(patterns: any): string[] {
  const suggestions: string[] = [];
  
  if (patterns.has_rss) {
    suggestions.push("✅ RSS feed detected - use standard RSS extraction");
  }
  
  if (patterns.has_wordpress) {
    suggestions.push("✅ WordPress detected - use WP REST API");
  }
  
  if (patterns.has_joomla) {
    suggestions.push("⚠️ Joomla detected - may need custom parser");
  }
  
  if (patterns.article_patterns.length > 0) {
    suggestions.push(`Found ${patterns.article_patterns.length} article patterns`);
    suggestions.push(`Try selector: ${patterns.article_patterns[0]}`);
  }
  
  if (patterns.meta_info.generator) {
    suggestions.push(`CMS: ${patterns.meta_info.generator}`);
  }
  
  if (suggestions.length === 0) {
    suggestions.push("⚠️ Unknown CMS - custom code generation may be needed");
  }
  
  return suggestions;
}

function findArticleSelectors(html: string): string[] {
  const selectors: string[] = [];
  const patterns = [
    /<article[^>]*class="([^"]*)"[^>]*>/gi,
    /<div[^>]*class="[^"]*(?:article|post|nota|noticia)[^"]*"[^>]*>/gi,
    /<div[^>]*class="[^"]*card[^"]*"[^>]*>/gi
  ];
  
  for (const pattern of patterns) {
    let match;
    while ((match = pattern.exec(html)) !== null) {
      if (match[0].length < 200) {
        selectors.push(match[0].slice(0, 100));
      }
    }
  }
  
  return [...new Set(selectors)].slice(0, 5);
}

function findTitleSelectors(html: string): string[] {
  const selectors: string[] = [];
  const patterns = [
    /<h[12][^>]*class="([^"]*)"[^>]*>/gi,
    /<a[^>]*class="[^"]*(?:title|headline)[^"]*"[^>]*>/gi
  ];
  
  for (const pattern of patterns) {
    let match;
    while ((match = pattern.exec(html)) !== null) {
      if (match[0].length < 150) {
        selectors.push(match[0].slice(0, 100));
      }
    }
  }
  
  return [...new Set(selectors)].slice(0, 5);
}

function findDateSelectors(html: string): string[] {
  const selectors: string[] = [];
  const patterns = [
    /<time[^>]*>/gi,
    /<span[^>]*class="[^"]*(?:date|time|fecha)[^"]*"[^>]*>/gi,
    /<meta[^>]*property="article:published_time"[^>]*>/gi
  ];
  
  for (const pattern of patterns) {
    let match;
    while ((match = pattern.exec(html)) !== null) {
      selectors.push(match[0].slice(0, 100));
    }
  }
  
  return [...new Set(selectors)].slice(0, 5);
}
