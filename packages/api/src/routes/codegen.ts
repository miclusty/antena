import { Hono } from "hono";
import type { Env } from "../lib/types";
import { authMiddleware } from "../middleware/auth";

export const codegenRoutes = new Hono<{ Bindings: Env }>();

/**
 * GET /api/extract/codegen/:sourceId
 * 
 * Returns site HTML structure for Hermes to analyze
 * and potentially generate custom extraction code
 */
codegenRoutes.get("/codegen/:sourceId", authMiddleware, async (c) => {
  const sourceId = parseInt(c.req.param("sourceId"));
  const db = c.env.DB;
  
  // Get source info
  const source = await db.prepare(
    "SELECT * FROM sources WHERE id = ?"
  ).bind(sourceId).first<any>();

  if (!source) {
    return c.json({ error: "Source not found" }, 404);
  }

  // Fetch page HTML (sample)
  try {
    const controller = new AbortController();
    setTimeout(() => controller.abort(), 15000);

    const response = await fetch(source.url, {
      signal: controller.signal,
      headers: {
        'User-Agent': 'Mozilla/5.0 (compatible; AKIRA/1.0)',
        'Accept': 'text/html'
      }
    });

    const html = await response.text();
    
    // Extract patterns for analysis
    const patterns = {
      has_rss: html.includes('/feed') || html.includes('/rss') || html.includes('atom.xml'),
      has_wordpress: html.includes('wp-content') || html.includes('wp-json'),
      has_joomla: html.includes('joomla') || html.includes('com_content'),
      article_patterns: extractPatterns(html),
      meta_info: {
        title: html.match(/<title>(.*?)<\/title>/)?.[1] || '',
        generator: html.match(/<meta name="generator" content="(.*?)"/)?.[1] || '',
        og_type: html.match(/<meta property="og:type" content="(.*?)"/)?.[1] || ''
      }
    };

    return c.json({
      source: {
        id: source.id,
        name: source.name,
        url: source.url,
        type: source.type
      },
      html_sample: html.slice(0, 5000), // First 5KB for analysis
      patterns,
      suggestions: generateSuggestions(patterns),
      extraction_hints: {
        possible_article_selectors: findArticleSelectors(html),
        possible_title_selectors: findTitleSelectors(html),
        possible_date_selectors: findDateSelectors(html)
      }
    });

  } catch (error) {
    return c.json({ 
      error: "Failed to fetch site",
      message: (error as Error).message 
    }, 500);
  }
});

